opcion = int(input("Ingrese un numero de 1 al 5: "))

if opcion == 1:
    print("Sumar dos números")
elif opcion == 2:
    print("Restar dos números")
elif opcion == 3: 
    print("Multiplicar dos números")
elif opcion == 4:
    print("Dividir dos números")
elif opcion == 5:
    print("Fin del programa")
else:
    print("Opción no válida")