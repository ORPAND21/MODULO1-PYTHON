x = int(input("Escribe un numero 1-7: "))

if x == 1:
    print("Domingo")
elif x == 2:
    print("Lunes")
elif x == 3:
    print("Martes")
elif x == 4:
    print("Miercoles")
elif x == 5:
    print("Jueves")
elif x == 6:
    print("Viernes")
elif x == 7:
    print("Sabado")
else:
    print("Dia invalido")