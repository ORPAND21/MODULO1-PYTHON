x = int(input("Ingresa un numero: "))
y = int(input("Ingresa un segundo numero: "))

suma = x + y
resta = x - y
multiplicacion = x * y
division = x/y

print(f"Tu suma es: {suma}")
print(f"Tu resta es: {resta}")
print(f"Tu multiplicacion es: {multiplicacion}")
print(f"Tu division es: {division:.0f}")